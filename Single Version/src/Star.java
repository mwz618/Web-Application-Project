

public class Star {
    private String name = "";
    private String birthYear = "";


    public Star() {

    }

    public String getBirthYear() {
        return birthYear;
    }

    public void setBirthYear(String year) {
        this.birthYear = year;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

}
